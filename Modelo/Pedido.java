package Modelo;

public class Pedido {
	private String burger;
	private String fries;
	private String drink;
	private double price;
	
	public Pedido(String burger, String fries, String drink, double price) {
		this.burger = burger;
		this.fries = fries;
		this.drink = drink;
		this.price = price;
	}
	
	
}
